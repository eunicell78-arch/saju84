def get_napeum(pillar_hanja: str) -> str:
    """
    간지로 납음오행 조회
    
    Args:
        pillar_hanja: 간지 한자 (예: '己丑')
    
    Returns:
        납음오행 (예: '벽력화(霹靂火)')
    """
    return NAPEUM.get(pillar_hanja, '알수없음')


def get_napeum_element(napeum: str) -> str:
    """
    납음오행에서 오행만 추출
    
    Args:
        napeum: 납음오행 전체 (예: '벽력화(霹靂火)')
    
    Returns:
        오행 (예: '화(火)')
    """
    if '금' in napeum or '金' in napeum:
        return '금(金)'
    elif '목' in napeum or '木' in napeum:
        return '목(木)'
    elif '수' in napeum or '水' in napeum:
        return '수(水)'
    elif '화' in napeum or '火' in napeum:
        return '화(火)'
    elif '토' in napeum or '土' in napeum:
        return '토(土)'
    else:
        return '알수없음'
