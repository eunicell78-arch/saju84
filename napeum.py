def get_napeum(pillar: str) -> str:
    """
    납음오행 조회
    
    Args:
        pillar: 간지 (예: '甲子', '己丑')
    
    Returns:
        납음오행 (예: '海中金', '霹靂火')
    """
    if pillar in NAPEUM_TABLE:
        napeum = NAPEUM_TABLE[pillar]
        korean = NAPEUM_KOREAN.get(napeum, '')
        return f"{napeum}({korean})"
    return '미상'


def get_all_napeum(year_pillar: str, month_pillar: str, 
                   day_pillar: str, hour_pillar: str) -> dict:
    """
    사주 전체 납음오행 조회
    
    Returns:
        {'year': '납음', 'month': '납음', 'day': '납음', 'hour': '납음'}
    """
    return {
        'year': get_napeum(year_pillar),
        'month': get_napeum(month_pillar),
        'day': get_napeum(day_pillar),
        'hour': get_napeum(hour_pillar)
    }
